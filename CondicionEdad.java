public class CondicionEdad {
    public static void main(String[] args) {
        int edad = 9;
        if (edad == 7)
            System.out.println("¡Guau! Tienes " + edad + " años, ¡increíble!");
        else if (edad == 8)
            System.out.println("Bueno, tienes " + edad + " años, tampoco está tan mal");
        else
            System.out.println("No tienes siete años, ni tampoco ocho, eres un fraude.");

    }
}
